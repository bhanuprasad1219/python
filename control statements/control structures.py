#selection control

n = int(input("enter a value:"))
if n<10:                            #condition
    print("less than n")            #printing n while if is true
else:                               #condition false
    print("greater than n")         #printing n while else is false

#iterative control

m = 10
while m < 20:  #initialisation condition implimentation
    print(m)   #printing m values
    m = m+1    # incrementing the values